#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		len;
	int		i;
	char	*new_str;

	i = 0;
	len = ft_strlen(str);
	new_str = (char *)malloc(sizeof(char) * (len + 1));
	if (!new_str)
		return (NULL);
	while (str[i])
	{
		new_str[i] = str[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
